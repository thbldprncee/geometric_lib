def area(a):
    """
    Calculate the area of a square.

    Args:
        a (float): Length of the side of the square. Must be a positive number.

    Returns:
        float: Area of the square calculated using the formula S = a * a
    """
    return a * a

def perimeter(a):
    """
    Calculate the perimeter of a square.

    Args:
        a (float): Length of the side of the square. Must be a positive number.

    Returns:
        float: Perimeter of the square calculated using the formula P = 4 * a
    """
    return 4 * a